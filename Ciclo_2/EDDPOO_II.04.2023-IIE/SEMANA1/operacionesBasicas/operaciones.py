def suma(num1, num2):
    
    suma = num1 + num2
    
    return suma

def resta(num1, num2):
    
    resta = num1 - num2
    
    return resta

def multiplicacion(num1, num2):
    
    multiplicacion = num1 * num2
    
    return multiplicacion

def division(num1, num2):
    
    division = num1 / num2
    
    return division

def potencia(num1, num2):
    
    potencia = num1 ** num2
    
    return potencia